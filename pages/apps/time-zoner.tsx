const Clock = ({zone ='' }) => {
    const [value, setValue] = useState('');
    let ref = useRef<any>();

    useEffect(() =>{
        ref.current = setInterval(()=>{
            let date = new Date();
            let strTime = date.toLocaleString('en-US', {
              timeZone: zone,
            });
            setValue(strTime);
        }, 1000)
        return (() =>{
            clearInterval(ref.current);
        })
    });
    
    return (
      <Card>
        {value ? (
          <div className=" animate__animated animate__fadeIn">
            <h6 className="text-sm text-center">{zone}</h6>
            <p className="pt-3 pb-3 text-xl font-bold text-center">
              {value.split(',')[0]}
            </p>

            <Badge
              value={value.split(',')[1]}
              severity="success"
              className="p-3"
              style={{ fontSize: 25, padding: 5, display: 'block', height: 33 }}
            />
          </div>
        ) : (
          <Skeleton height="7rem"/>
        )}
      </Card>
    );
}


const TimeZoner = () => {
  const [filter, setFilter] = useState([...aryIannaTimeZones]);
  const [zone,setZone] = useState('');

  const onZoneChange = (e) => {
    let {value} = e.target;
    setZone(value);
    let result = aryIannaTimeZones.filter((zone) => zone.toLowerCase().includes(value));
    setFilter(result);
  }
    return (
      <>
        <main className=" border-2 bg-slate-100 h-600">
          <div className="shadow p-2 bg-white">
            <Breadcrumb />
            <br />
            <div className="flex justify-between">
              <h1 className=" text-xl font-extrabold font-sans  text-violet-900 antialiased">
                Time zoner
              </h1>
              <ShareArticle />
            </div>
            <div className="field">
              <label htmlFor="timezone" className="block">
                Search timezone
              </label>
              <InputText
                value={zone}
                onChange={onZoneChange}
                id="timezone"
                aria-describedby="timezone-search"
                className="block"
                placeholder="Enter timezone"
              />
            </div>
          </div>
        </main>

        <Card
          className="grow"
          style={{
            backgroundColor: `rgb(20, ${Math.floor(
              Math.random() * 100
            )}, ${Math.floor(Math.random() * 100)})`,
          }}
        >
          <div className="relative grid grid-cols-2 sm:grid-cols-3 md:grid-cols-5 lg:grid-cols-5 xl:grid-cols-5 gap-2">
            {filter.map((zone, idx) => {
              return (
                <div
                  key={idx}
                  className="m-2 animate__animated animate__fadeInLeft"
                >
                  <Clock zone={zone} />
                </div>
              );
            })}
          </div>
        </Card>
      </>
    );
}

TimeZoner.getLayout = (page) => <GeneralLayout>{page}</GeneralLayout>;

export default TimeZoner;